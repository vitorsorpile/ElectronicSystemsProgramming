HAL_StatusTypeDef Send_Message_via_UART(Message_t *msg, UART_HandleTypeDef *huart) {
	uint8_t aux[13];
	memcpy(aux, msg, 13);
	return HAL_UART_Transmit_IT(huart, aux, 13);
}
